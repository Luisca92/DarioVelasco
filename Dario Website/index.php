							<article id="Campo">
								<h2 class="major">Asistente de Campo</h2>
								<span class="image main"><img src="images/Dario4.jpg" alt="" /></span>
								<p>Soy trabajador externo asociado de la Estación de Biología de Los Tuxtlas. 
									Desde mi adolescencia he trabajado como asistente de campo para los estudiantes e investigadores de varias universidades de México y del mundo que vienen a hacer su investigación en Los Tuxtlas. 
									Soy bilingüe (español e inglés), y mi tarifa de trabajo es $250 pesos por día.</p>
								<h3>Destrezas Relevantes</h3>
								<h4>Botánica</h4>
									<p> Estoy muy familiarizado con las especies de plantas nativas a la región de Los Tuxtlas. La mayoría las conozco por nombre común, pero también algunos con el nombre cientifico. 
									Si requieres semillas para un experimento de germinación, te puedo asesorar en cuales especies están fructificando, cuando, como germinar las semillas, y por supuesto, las puedo colectar y cuidar una vez germinadas. 	
								</p>
								<h4>Biología Acuática</h4>
								<p> Soy experienciado en la colecta de invertebrados y vertebrados acuáticos. Si necesitas colectar o muestrar 
									en las lagunas de Los Tuxtlas o en la costa, yo tengo buenas técnicas para el manejo de redes (chinchorro y atarraya). 
									También se discernir entre peces adultos y juveniles, y siempre trato a toda la fauna, hasta el molusco más pequeño, con el merecido cuidado y respeto.
								</p>

								<h4>Manualidades & Transporte</h4>
								<p>Soy hábil carpintero para casi cualquier necesidad (por ejemplo, para hacer estacas, reparar cercas, seccionar experimentos de germinación, y más). 
									Si requieres ayuda consiguiendo materiales también te puedo ayudar, con tal de que me avises con tiempo, con especificaciones, y con tal de que me compenses los gastos.
									Tengo mi propia motocicleta, canoa, y también un Ferrari (mi burrito), por si es necesario transportar personas o equipo por la región. 
								</p>
								<h4>Manejo de Datos</h4>
								<p>Entiendo y sé lo indispensables que son los datos para los biólogos. Por esa razón me esfuerzo en tomar datos exactamente como me los piden, con buena letra, y haciendo un backup fotográfico periodicamente si me lo piden.
									Puedes confiar que tus datos están sanos, salvos, y correctos cuando los manejo yo. 
								</p>
