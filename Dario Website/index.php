								<h2 class="major">Turismo: Laguna Azul</h2>
								<p> Ofrezco viajes en canoa para cruzar a travez de Laguna Escondida y llegar a una de las bellezas naturales de la región, Laguna Azul.</p>
								<span class="image main"><img src="images/Laguna.jpg" alt="" /></span>
								<p> Nos esforzamos por mantener a Laguna Azul bien pristina para que todos sus visitantes la puedan disfrutar al máximo. 
									Sus aguas frescas, famosamente azules, son perfectas para las tardes de calor tropical, y junto a los cantos de las aves tropicales de Veracruz, te sentiras verdaderamente renovado.
									Como visitante, te pedimos que seas respetuoso del sitio, llevandote toda tu basura, y sin lastimar o llevarte fauna o flora del lugar.
									<br> <br> ¿Quieres ir? ¡Pues vamos! Ponte en contacto o visitame en Laguna Escondida. Por canoa el valor es $250 pesos. </p>
								<span class="image main"><img src="images/Laguna2.jpg" alt="" /></span>
								<p>También, si quieren almorzar, aquí en mi casa con gusto les preparamos algo rico a precio razonable (hablarlo anticipadamente para conseguir ingredientes). </p>
